// ─── Core i18n API ────────────────────────────────────────────────────────────

let currentLanguage = localStorage.getItem('language') || 'en';

/**
 * Returns the translated string for the given key.
 * Falls back to English, then the key itself.
 */
function t(key) {
    const lang = TRANSLATIONS[currentLanguage];
    if (lang && lang[key] !== undefined) return lang[key];
    const en = TRANSLATIONS['en'];
    if (en && en[key] !== undefined) return en[key];
    return key;
}

/**
 * Applies translations to all elements with data-i18n* attributes.
 * data-i18n          → textContent
 * data-i18n-html     → innerHTML
 * data-i18n-placeholder → placeholder attribute
 * data-i18n-title    → title attribute
 */
function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        el.textContent = t(el.dataset.i18n);
    });
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
        el.innerHTML = t(el.dataset.i18nHtml);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        el.placeholder = t(el.dataset.i18nPlaceholder);
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        el.title = t(el.dataset.i18nTitle);
    });
}

/**
 * Builds and injects the language <select> into #language-select-container.
 */
function buildLanguageSelector() {
    const container = document.getElementById('language-select-container');
    if (!container) return;

    const select = document.createElement('select');
    select.id = 'settings-language';
    select.style.cssText = 'width:100%;padding:10px;background:var(--bg-dark);border:2px solid var(--bg-light);border-radius:8px;color:var(--text-primary);font-size:14px;cursor:pointer;';

    SUPPORTED_LANGUAGES.forEach(lang => {
        const opt = document.createElement('option');
        opt.value = lang.code;
        opt.textContent = lang.name;
        if (lang.code === currentLanguage) opt.selected = true;
        select.appendChild(opt);
    });

    container.appendChild(select);
}

